package Entidad;
public class Mesa {
    private String id_mesa;
    private String colegio;
    private String numero_salon;
    private String numero_mesa;
    public String getId_mesa() {
        return id_mesa;
    }
    public void setId_mesa(String id_mesa) {
        this.id_mesa = id_mesa;
    }
    public String getColegio() {
        return colegio;
    }
    public void setColegio(String colegio) {
        this.colegio = colegio;
    }
    public String getNumero_salon() {
        return numero_salon;
    }
    public void setNumero_salon(String numero_salon) {
        this.numero_salon = numero_salon;
    }
    public String getNumero_mesa() {
        return numero_mesa;
    }
    public void setNumero_mesa(String numero_mesa) {
        this.numero_mesa = numero_mesa;
    }
}
